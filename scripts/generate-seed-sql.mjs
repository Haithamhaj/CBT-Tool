import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const casesPath = path.join(root, "data/seeds/cases.json");
const usersPath = path.join(root, "data/seeds/users.json");
const outputPath = path.join(root, "supabase/seed.sql");

const cases = JSON.parse(fs.readFileSync(casesPath, "utf8"));
const users = JSON.parse(fs.readFileSync(usersPath, "utf8"));

function sqlString(value) {
  return `'${String(value).replaceAll("'", "''")}'`;
}

function sqlNullable(value) {
  return value === null ? "null" : sqlString(value);
}

function sqlJson(value) {
  return `${sqlString(JSON.stringify(value))}::jsonb`;
}

function buildUserRow(user) {
  return `(${sqlString(user.id)}, ${sqlString(user.email)}, ${sqlString(user.name)}, ${sqlString(user.role)}, ${sqlString(user.level)}, ${sqlNullable(user.facilitator_id)})`;
}

function buildCaseRow(caseRecord) {
  return `(
  ${sqlString(caseRecord.id)},
  ${sqlString(caseRecord.title)},
  ${sqlString(caseRecord.difficulty)},
  ${sqlString(caseRecord.theme)},
  ${sqlString(caseRecord.presenting_complaint)},
  ${sqlJson(caseRecord.trigger_events)},
  ${sqlJson(caseRecord.sample_thoughts)},
  ${sqlJson(caseRecord.sample_emotions)},
  ${sqlJson(caseRecord.sample_behaviors)},
  ${sqlJson(caseRecord.hidden_beliefs)},
  ${sqlJson(caseRecord.recommended_tools)},
  ${sqlJson(caseRecord.expected_drifts)},
  ${caseRecord.homework_context_present ? "true" : "false"},
  ${sqlJson(caseRecord.stage_suitability)},
  ${sqlJson(caseRecord)}
)`;
}

const sql = `-- Generated by scripts/generate-seed-sql.mjs
begin;

insert into public.users (id, email, name, role, level, facilitator_id)
values
${users.map(buildUserRow).join(",\n")}
on conflict (id) do update
set
  email = excluded.email,
  name = excluded.name,
  role = excluded.role,
  level = excluded.level,
  facilitator_id = excluded.facilitator_id,
  updated_at = timezone('utc', now());

insert into public.cases (
  id,
  title,
  difficulty,
  theme,
  presenting_complaint,
  trigger_events,
  sample_thoughts,
  sample_emotions,
  sample_behaviors,
  hidden_beliefs,
  recommended_tools,
  expected_drifts,
  homework_context_present,
  stage_suitability,
  content_json
)
values
${cases.map(buildCaseRow).join(",\n")}
on conflict (id) do update
set
  title = excluded.title,
  difficulty = excluded.difficulty,
  theme = excluded.theme,
  presenting_complaint = excluded.presenting_complaint,
  trigger_events = excluded.trigger_events,
  sample_thoughts = excluded.sample_thoughts,
  sample_emotions = excluded.sample_emotions,
  sample_behaviors = excluded.sample_behaviors,
  hidden_beliefs = excluded.hidden_beliefs,
  recommended_tools = excluded.recommended_tools,
  expected_drifts = excluded.expected_drifts,
  homework_context_present = excluded.homework_context_present,
  stage_suitability = excluded.stage_suitability,
  content_json = excluded.content_json,
  updated_at = timezone('utc', now());

commit;
`;

fs.writeFileSync(outputPath, sql, "utf8");
console.log(`Wrote ${outputPath}`);
